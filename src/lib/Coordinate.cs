using System;
namespace lib
{
    public class Coordinate
    {
        public int x = 0;
        public int y = 0;

        public Coordinate()
        {
            this.x = 0;
            this.y = 0;
        }
        public Coordinate(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
    }
}